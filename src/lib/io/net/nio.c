#include "nio.h"

#include <winsock2.h>

#include "../../log/log.h"

static WSADATA sowr_net_wsadata;

int
sowr_Net_Init( void )
{
#ifdef SOWR_TARGET_WINDOWS
    int result = WSAStartup(MAKEWORD(2, 2), &sowr_net_wsadata);
    if (result)
        SOWR_LOG_ERROR("Failed to initialize Winsock!");
    return result;
#endif
}

void
sowr_Net_CleanUp( void )
{
#ifdef SOWR_TARGET_WINDOWS
    WSACleanup();
#endif
}

#include "nio.h"

#include "../../../log/log.h"

#ifdef BLRT_TARGET_WINDOWS
    #include <winsock2.h>

    static WSADATA blrt_net_wsadata;
#endif

int
blrt_Net_Init( void )
{
#ifdef BLRT_TARGET_WINDOWS
    return WSAStartup(MAKEWORD(2, 2), &blrt_net_wsadata);
#else
    return 0;
#endif
}

void
blrt_Net_CleanUp( void )
{
#ifdef BLRT_TARGET_WINDOWS
    WSACleanup();
#endif
}
